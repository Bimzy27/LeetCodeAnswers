class LogHelper:
    @staticmethod
    def PrintOutput(output):
        print(f"Output -> {output}")

    @staticmethod
    def PrintInput(input):
        print(f"Input -> {input}")

    @staticmethod
    def PrintExpected(expected):
        print(f"Expected -> {expected}")

    @staticmethod
    def Print(msg):
        print(f"Message -> {msg}")